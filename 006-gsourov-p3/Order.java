/**
* An enumeration representing order types, such as ascending and descending.
*/
public enum Order
{
	/**
	* Preorder traversal order: Visit the current node before its children.
	*/
	PREORDER,
	/**
	* Preorder traversal order: Visit the current node before its children.
	*/
	INORDER,
	/**
	* Preorder traversal order: Visit the current node before its children.
	*/
	POSTORDER;
}
